interface WalletCheckerProps {
  address?: string
}

export default function WalletChecker({ address }: WalletCheckerProps) {
  const { isEligible, maxAllowed } = useMemo(() => {
    if (!address) return { isEligible: false, maxAllowed: 0 }
    
    const normalizedAddress = address.toLowerCase()
    const found = Array.from(ELIGIBLE_WALLETS.entries()).find(
      ([wallet]) => wallet.toLowerCase() === normalizedAddress
    )

    if (found) {
      return { isEligible: true, maxAllowed: found[1] }
    }

    return { isEligible: false, maxAllowed: 0 }
  }, [address])

  const shortAddress = address ? `${address.slice(0, 6)}...${address.slice(-4)}` : ''

  return (
    <div className="space-y-6">
      {/* Wallet Address Display */}
      <div className="bg-gradient-to-r from-slate-100 to-slate-50 rounded-lg p-4">
        <p className="text-sm text-gray-600 mb-1">Connected Wallet</p>
        <p className="font-mono text-lg font-semibold text-gray-900">{shortAddress}</p>
      </div>

      {/* Eligibility Status */}
      <div className={`rounded-lg p-6 transform transition-all duration-300 ${
        isEligible 
          ? 'bg-gradient-to-br from-emerald-50 to-teal-50 border-2 border-emerald-200'
          : 'bg-gradient-to-br from-red-50 to-pink-50 border-2 border-red-200'
      }`}>
        <div className="flex items-center gap-3 mb-3">
          {isEligible ? (
            <div className="flex items-center justify-center w-10 h-10 rounded-full bg-emerald-500">
              <Check className="w-6 h-6 text-white" />
            </div>
          ) : (
            <div className="flex items-center justify-center w-10 h-10 rounded-full bg-red-500">
              <X className="w-6 h-6 text-white" />
            </div>
          )}
          <div>
            <p className={`text-xl font-bold ${
              isEligible ? 'text-emerald-900' : 'text-red-900'
            }`}>
              {isEligible ? 'Eligible ✓' : 'Not Eligible'}
            </p>
          </div>
        </div>

        {isEligible && (
          <div className="bg-white/60 backdrop-blur rounded-md p-3 mt-4">
            <p className="text-sm text-gray-600">Max Mint Amount</p>
            <p className="text-2xl font-bold text-emerald-600">{maxAllowed}</p>
          </div>
        )}

        {!isEligible && (
          <p className="text-sm text-red-700 mt-2">
            This wallet is not on the FCFS eligible list
          </p>
        )}
      </div>
    </div>
  )
}
